          <p ><b>ความหมายของ front-end</b> 
            <P></P>สำหรับ developer จะเรียกกันสั้น ๆ ว่า 
            หน้าบ้าน หรือเป็นส่วนติดต่อผู้ใช้ (User interface) ไม่ว่าจะเป็น หน้าโฮม หน้าเว็บเพจ 
            เนื้อหาต่างๆ รูปภาพ ลิงก์ เป็นต้น เป็นส่วนที่ user ทั่วไปสามารถเห็นและเข้ามาใช้งานได้ของเว็บไซต์</p>
          <br>
          <p ><b>ความสำคัญของ front-end </b> ส่วนนี้จะแสดงหน้าตาของเว็บไซต์ให้ผู้เข้าชมเห็น 
