          <p><b>ความหมายของ back-end</b> สำหรับ developer จะเรียกกันสั้น ๆ ว่า หลังบ้าน
            หรือระบบจัดการเว็บไซต์ เช่น จัดการฐานข้อมูล โครงสร้างเว็บไซต์ การเขียน
            โค้ดควบคุม XML, text file, JAVA, PHP, C#, C++ เป็นต้น จะมีไว้สำหรับ
            และเข้ามาใช้งานได้ของเว็บไซต์</p>
          <br>
          <p ><b>ความสำคัญของ back-end</b> ส่วนของ back-end มีความสำคัญเป็นอย่างยิ่ง
            ไม่ว่าจะเป็นการรักษาความปลอดภัยของข้อมูล ทั้ง username password  
            ข้อมูลเว็บไซต์ต่างๆ หาก backend มีการ update ก็ต้องทำการ backup      
            ข้อมูลเดิมเก็บไว้ และตรวจสอบให้ดีก่อนทำการ update รวมไปถึงการทำงานของเว็บไซต์ ความเร็วในการแสดงผล</p>
          <br>
          <h2 class="text1"><b>คนที่ทำงานด้าน back-end ควรต้องมีทักษะด้านใดบ้าง</b></h2>
          <p>1. Web Developer (Javascript, CSS, HTML5, jQuery)</p>
          <br>
          <hr>
          <h2 class="text1"><b>Front-end และ Back-end เกี่ยวข้องกันอย่างไร</b></h2>
          <p>front-end กับ back-end จะมีความสำคัญต่อกันเป็นอย่างมาก หาก front-end แสดงผลช้าหรือเกิดข้อผิดพลาด 
            ส่วนของ back-end ก็ต้องทำการแก้ไขปัญหา หรือถ้าหากว่า back-end ไม่ทำการปรับปรุงเว็บไซต์อย่างสม่ำเสมอ จะส่งผลต่อเว็บไซต์เป็นอย่างมาก 
            ไม่ว่าจะเป็น เว็บไซต์ล้าหลัง ขาดความน่าสนใจ ผู้เยี่ยมชมลดลง ก่อให้เกิดผลกระทบหลาย ๆ ด้าน </p>
          <br>
